#include "libft.h"

void	ft_strclr(char *s)
{
	int		size;
	int		i;

	if (s)
	{
		size = ft_strlen(s);
		i = size - 1;
		while (i >= 0)
		{
			s[i] = '\0';
			i--;
		}
	}
}
